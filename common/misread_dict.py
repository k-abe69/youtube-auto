def apply_misread_corrections(text: str) -> str:
    """
    登録された読み間違い補正辞書を元に、全文一致で置換する
    """
    for wrong, corrected in MISREAD_REPLACEMENTS.items():
        text = text.replace(wrong, corrected)
    return text
